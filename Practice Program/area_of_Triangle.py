# Topic - calculate the area of a triangle

# Area of triangle = 1/2 * base * height

# method - 1 : pre-defined dimension of triangle

# Dimension of the triangle
base = 8
height = 10

# formula of triangle
area = (1/2) * base * height

# print the result
print("The area of triangle : ", area)

# method - 2 : take the input from the user

# take input
triangle_base = float(input("Enter the base : "))
triangle_height = float(input("Enter the height : "))

area_of_triangle = (1/2) * triangle_base * triangle_height

# print the output
print("The area of triangle is : ", area_of_triangle)