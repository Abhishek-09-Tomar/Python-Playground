# Topic - Tuples and operations on Tuples in Python
"""tuple is a set of ordered element but it is immutable.

Immutable : means cannot be changed like tuple
mutable : means can be changed like list"""

my_tuple = (19,43,54,23,43)
print("Given Tuple is : ",my_tuple)

# create a tuple with single element.
single_tuple = (23,) # single element will be stored in the tuple with the comma(,).
print(single_tuple)

# here we can also do slicing of a tuple.

print(my_tuple[2]) # it will print the element present at the index 2. (output : 54)











