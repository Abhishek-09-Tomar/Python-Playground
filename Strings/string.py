# Topic - Strings in python

# String : A string is simply a series of characters. Anything inside quotes is considered a string in Python, and you can use single or double quotes around your strings like this: "This is a string." 'This is also a string.'


# These are the three ways to store the string in python.
name = "Harry"
name = 'Harry'
name = '''Harry'''
print(name)

# Counting in programming is starts with zero.

print(name[0]) # They print the first letter of the string.

# Negative indices in string.
print(name[-1]) # output:y

